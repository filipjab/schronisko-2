                    <p>Policja, straż gminna, straż miejska, schronisko – to służby, które są odpowiednio przeszkolone do reakcji w takich sytuacjach. Twoim obowiązkiem prawnym jest poinformować jeden z tych organów w ciągu 14 dni od znalezienia zwierzaka.

Jeśli zaś kojarzysz, że zwierzę jest poszukiwane przez właściciela, skontaktuj się z nim bezpośrednio i poinformuj, gdzie może się po niego zgłosić.

