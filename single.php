Najważniejsze jest zachowanie własnego bezpieczeństwa w takiej sytuacji. Bezpańskie zwierzę może być groźne, ze względu na to, że czuje się zagrożone. Psy niekiedy sygnalizują swój strach poprzez szczerzenie zębów, warczenie, szczekanie i przybieranie groźnej postawy. To naturalny, obronny zabieg, który ma na celu odstraszenie potencjalnego zagrożenia.

