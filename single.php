Bezdomne zwierzęta – jak wyglądają statystyki?
Zgodnie z raportem rocznym Głównego Lekarza Weterynarii z wizytacji schronisk dla zwierząt w 2020 roku w schroniskach przebywało 92 242 psów oraz 32 507 kotów. W tym roku adoptowano 63 358 psów oraz 21 245 kotów ze schronisk.

Z kolei w 2021 roku w schronisku zarejestrowano 82 316 psów oraz 33 367 kotów, odsetek spadł o 11% (psy) i wzrósł o ok. 2% (koty). W 2021 adoptowano 53 961 psów oraz 20 237 kotów.

To oczywiście nie wszystkie bezdomne zwierzęta, które żyją w Polsce. Wiele z nich trafia również do domów tymczasowych, fundacji lub po prostu żyje na ulicach.</p>
